const jwt = require('jsonwebtoken');
const { User } = require('../models');

const authenticateToken = async (req, res, next) => {
  const authHeader = req.headers['authorization'];
  const token = authHeader && authHeader.split(' ')[1];

  if (!token) {
    return res.status(401).json({ message: 'Access token required' });
  }

  try {
    const decoded = jwt.verify(token, process.env.JWT_SECRET);
    const user = await User.findByPk(decoded.userId);
    
    if (!user || !user.isActive) {
      return res.status(401).json({ message: 'Invalid or inactive user' });
    }

    req.user = user;
    next();
  } catch (error) {
    return res.status(403).json({ message: 'Invalid token' });
  }
};

const authorizeRoles = (...roles) => {
  return (req, res, next) => {
    if (!req.user) {
      return res.status(401).json({ message: 'Authentication required' });
    }

    if (!roles.includes(req.user.role)) {
      return res.status(403).json({ 
        message: 'Insufficient permissions',
        required: roles,
        current: req.user.role
      });
    }

    next();
  };
};

const authorizeAdmin = authorizeRoles('admin');
const authorizeTester = authorizeRoles('admin', 'tester');
const authorizeQA = authorizeRoles('admin', 'qa_manager');
const authorizeCompany = authorizeRoles('admin', 'tester', 'qa_manager', 'company');

module.exports = {
  authenticateToken,
  authorizeRoles,
  authorizeAdmin,
  authorizeTester,
  authorizeQA,
  authorizeCompany
};
